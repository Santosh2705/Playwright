console.log(typeof 123); // In Javascript there is no concept called int and float, The int and float number are always represented as numbers
console.log(typeof 31.34)
console.log(typeof "Bangalore");
console.log(typeof undefined); // Output will be undefined
console.log(typeof null); // Output will be Object, Null is defined as an Object
console.log(typeof []); // Output will be Object, Since the arrays are also treated as an Object